package hyrpractice;

public class divisible2 {

	public static void main(String[] args) {
		int num =8;
		if(num % 3==0)
		{
			System.out.println("divisible by 3");
		}
		else {
			System.out.println("not divisible by 3");
		}
		if(num % 4==0)
		{
			System.out.println("divisible by 4");
		}
		else {
			System.out.println("not divisible by 4");
		}
	}

}
