package hyrpractice;

public class divisible {

	public static void main(String[] args) {
		int num = 10;
		if(num % 2 ==0)
		{
			System.out.println("divisible by 2");
		}
		else {
			System.out.println("not divisible 2");
		}
	
	if(num % 4 ==0)
	{
		System.out.println("divisible by 4");
	}
	else {
		System.out.println("not divisible 4 ");
	}

}
}
